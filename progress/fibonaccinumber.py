class Solution:
    def fib(self, n: int) -> int:
        if n == 0: l == 0
        if n == 1: l == 1
        if n == 2:
            l = 1 + 0
            return l
        elif n == 3:
            l = 1 + 1
            return l
        elif n == 4:
            l = 2 + 1
            return l
        return l
